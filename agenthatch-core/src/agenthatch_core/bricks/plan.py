"""PlanLayer — State machine + structured planning for agenthatch agents.

v0.9.8: Implements the PLAN_GUIDED loop engine.  Agents running in plan_guided
mode first generate a structured plan (via a virtual `plan` tool), then execute
steps with explicit state tracking, repurposing detection, and verification.

Design decisions:
- Plan is generated by the LLM via a `plan` tool call at session start
- State transitions are managed by the loop, not the LLM
- Steps are linear (no DAG) — sufficient for most single-agent tasks
- Repurposing is triggered by consecutive tool failures or step blockage
- Lightweight (~250 lines) — no sub-agent delegation or token budgets
"""

from __future__ import annotations

import json
import logging
from datetime import datetime
from enum import Enum
from typing import Any, Optional

from pydantic import BaseModel, Field

logger = logging.getLogger(__name__)


# ---------------------------------------------------------------------------
# Agent State Machine
# ---------------------------------------------------------------------------

class AgentState(str, Enum):
    """States an agent can be in during plan-guided execution."""

    STARTING = "starting"       # Initial state, waiting for plan
    PLANNING = "planning"       # Agent is generating/updating the plan
    EXECUTING = "executing"     # Agent is executing plan steps
    VERIFYING = "verifying"     # Agent is checking results
    REPLANNING = "replanning"   # Agent encountered a blockage, revising plan
    DONE = "done"               # Terminal state — all steps completed


# ---------------------------------------------------------------------------
# PlanStep
# ---------------------------------------------------------------------------

class PlanStep(BaseModel):
    """A single step in a structured plan."""

    step_id: int = 0
    description: str = ""
    tool_hint: str | None = None   # Recommended tool name (optional)
    status: str = "pending"         # pending | running | done | blocked
    result_summary: str = ""        # Brief outcome after step completes


# ---------------------------------------------------------------------------
# StructuredPlan
# ---------------------------------------------------------------------------

class StructuredPlan(BaseModel):
    """A complete structured plan with multiple steps."""

    goal: str = ""                  # One-sentence goal description
    steps: list[PlanStep] = Field(default_factory=list)
    current_step_index: int = 0     # Index of the currently executing step
    created_at: str = ""
    updated_at: str = ""

    @property
    def current_step(self) -> PlanStep | None:
        if 0 <= self.current_step_index < len(self.steps):
            return self.steps[self.current_step_index]
        return None

    @property
    def total_steps(self) -> int:
        return len(self.steps)

    @property
    def completed_steps(self) -> int:
        return sum(1 for s in self.steps if s.status == "done")

    @property
    def blocked_steps(self) -> int:
        return sum(1 for s in self.steps if s.status == "blocked")

    @property
    def is_complete(self) -> bool:
        return all(s.status == "done" for s in self.steps)

    def to_context_text(self) -> str:
        """Render the plan as text for system prompt injection."""
        lines = [f"## Plan: {self.goal}"]
        for step in self.steps:
            marker = {
                "pending": "☐",
                "running": "▶",
                "done": "✓",
                "blocked": "✗",
            }.get(step.status, "?")
            line = f"  {marker} Step {step.step_id}: {step.description}"
            if step.result_summary:
                line += f" — {step.result_summary}"
            lines.append(line)
        lines.append(f"Progress: {self.completed_steps}/{self.total_steps} steps done")
        return "\n".join(lines)


